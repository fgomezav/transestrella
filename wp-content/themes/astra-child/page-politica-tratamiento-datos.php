    <div class="policy-section" id="sec-4">
      <div class="section-header">
        <span class="section-num">04</span>
        <h2 class="section-title">Tipos de Datos Personales Tratados</h2>
      </div>
      <p>Transportadora Estrella S.A.S. trata las siguientes categorías de datos personales, dependiendo de la relación con el titular:</p>
      <ul>
        <li><strong>Datos de identificación:</strong> nombre completo, número de identificación, fecha de nacimiento.</li>
        <li><strong>Datos de contacto:</strong> dirección, teléfono, correo electrónico.</li>
        <li><strong>Datos laborales y comerciales:</strong> empresa, cargo, historial de servicio, registros de operaciones logísticas.</li>
        <li><strong>Datos financieros:</strong> información de facturación, cuentas bancarias para pagos y recaudos.</li>
        <li><strong>Datos de conductores y operadores:</strong> licencia de conducción, categoría, hoja de vida, registros de formación en el SENA.</li>
        <li><strong>Datos de vehículos:</strong> placa, SOAT, revisión técnico-mecánica, propietario.</li>
      </ul>
      <div class="callout callout-gold">
        <p><strong>Nota sobre datos sensibles:</strong> Transportadora Estrella S.A.S. podrá tratar datos sensibles (como datos de salud de trabajadores en el marco del Sistema de Gestión de Seguridad y Salud en el Trabajo) únicamente con autorización expresa del titular y según lo establece el artículo 6 de la Ley 1581 de 2012. El titular no está obligado a suministrarlos.</p>
