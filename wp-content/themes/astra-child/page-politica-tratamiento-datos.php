        <span class="section-num">03</span>
        <h2 class="section-title">Principios para el Tratamiento</h2>
      </div>
      <p>Transportadora Estrella S.A.S. aplicará los siguientes principios en todo tratamiento de datos personales:</p>
      <div class="principles-list">
        <div class="principle"><span class="principle-letter">a</span><div><div class="principle-name">Legalidad</div><div class="principle-desc">El tratamiento es una actividad reglada que debe sujetarse a las disposiciones legales vigentes.</div></div></div>
        <div class="principle"><span class="principle-letter">b</span><div><div class="principle-name">Finalidad</div><div class="principle-desc">El tratamiento obedecerá a una finalidad legítima informada al titular al momento de la recolección.</div></div></div>
        <div class="principle"><span class="principle-letter">c</span><div><div class="principle-name">Libertad</div><div class="principle-desc">El tratamiento solo puede ejercerse con el consentimiento previo, expreso e informado del titular.</div></div></div>
        <div class="principle"><span class="principle-letter">d</span><div><div class="principle-name">Veracidad o calidad</div><div class="principle-desc">Los datos sujetos a tratamiento deben ser veraces, completos, exactos, actualizados y comprobables.</div></div></div>
        <div class="principle"><span class="principle-letter">e</span><div><div class="principle-name">Transparencia</div><div class="principle-desc">El titular tiene derecho a obtener información sobre los datos que reposan en bases de datos y sobre el tratamiento.</div></div></div>
        <div class="principle"><span class="principle-letter">f</span><div><div class="principle-name">Acceso y circulación restringida</div><div class="principle-desc">El tratamiento solo podrá hacerse por personas autorizadas por el titular o por las personas previstas en la ley.</div></div></div>
        <div class="principle"><span class="principle-letter">g</span><div><div class="principle-name">Seguridad</div><div class="principle-desc">Los datos se protegerán mediante medidas técnicas, humanas y administrativas para evitar accesos no autorizados.</div></div></div>
        <div class="principle"><span class="principle-letter">h</span><div><div class="principle-name">Confidencialidad</div><div class="principle-desc">Toda persona que intervenga en el tratamiento está obligada a garantizar la reserva de la información.</div></div></div>
      </div>
    </div>

