        <li>Cumplimiento de obligaciones legales ante el Ministerio de Transporte, RNDC, DIAN y demás entidades regulatorias.</li>
        <li>Vinculación, gestión y bienestar de empleados y conductores.</li>
        <li>Envío de comunicaciones comerciales, cotizaciones y novedades de servicio.</li>
        <li>Realización de encuestas de satisfacción y estudios de mercado.</li>
        <li>Gestión de PQRS (peticiones, quejas, reclamos y sugerencias).</li>
        <li>Cumplimiento de los requisitos de las certificaciones ISO 9001 y BASC.</li>
      </ul>
