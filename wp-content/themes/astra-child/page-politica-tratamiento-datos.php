    <div class="policy-section" id="sec-2">
      <div class="section-header">
        <span class="section-num">02</span>
        <h2 class="section-title">Definiciones</h2>
      </div>
      <p>Para la correcta interpretación de esta política se adoptan las siguientes definiciones conforme a la Ley 1581 de 2012:</p>
      <ul>
        <li><strong>Autorización:</strong> consentimiento previo, expreso e informado del titular para llevar a cabo el tratamiento de datos personales.</li>
        <li><strong>Base de datos:</strong> conjunto organizado de datos personales que sea objeto de tratamiento.</li>
        <li><strong>Dato personal:</strong> cualquier información vinculada o que pueda asociarse a una o varias personas naturales determinadas o determinables.</li>
        <li><strong>Dato sensible:</strong> dato que afecta la intimidad del titular o cuyo uso indebido puede generar discriminación (origen racial, estado de salud, orientación sexual, datos biométricos, entre otros).</li>
        <li><strong>Encargado del tratamiento:</strong> persona natural o jurídica que realiza el tratamiento de datos personales por cuenta del responsable.</li>
        <li><strong>Responsable del tratamiento:</strong> persona natural o jurídica que decida sobre la base de datos y/o el tratamiento de los datos.</li>
        <li><strong>Titular:</strong> persona natural cuyos datos personales sean objeto de tratamiento.</li>
        <li><strong>Tratamiento:</strong> cualquier operación sobre datos personales (recolección, almacenamiento, uso, circulación, supresión, entre otras).</li>
      </ul>
    </div>

